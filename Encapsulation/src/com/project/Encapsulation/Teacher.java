package com.project.Encapsulation;

public class Teacher {
	int id;
	String name;
	String add;
	float sal;
	void setid(int id) {
		this.id=id;
	}
int getid() {
	return id;
}
	void setname(String name) {
		this.name=name;
	}
	String getname() {
		return name;
	}
	void setadd(String add) {
		this.add=add;
	}
	String getadd() {
		return add;
	}
	void setsal(float sal) {
		this.sal=sal;
	}
	float getsal() {
		return sal;
	}
}
