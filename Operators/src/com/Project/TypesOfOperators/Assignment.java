package com.Project.TypesOfOperators;

public class Assignment {
	public static void main(String[] args) {
		int a=50;
		int b=25;
		System.out.println(a);
		a+=b;
		System.out.println(a);
		a-=b;
		System.out.println(a);
		a*=b;
		System.out.println(a);
		a/=b;
		System.out.println(a);
	}

}