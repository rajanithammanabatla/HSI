package com.Project.TypesOfOperators;

public class Unary {
	public static void main(String[] args) {
		int a=50;
		int b=20;
		int c=10;
		boolean value=true;
		System.out.println(a);
		a=-a;
		System.out.println(a);
		System.out.println(b);
		b++;
		System.out.println(b);
		System.out.println(c);
		c--;
		System.out.println(c);
		System.out.println(value);
		System.out.println(!value);
	}

}
